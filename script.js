const textNodes = [];
const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
while (walker.nextNode()) {
  const node = walker.currentNode;
  const key = node.nodeValue.trim();
  if (translations[key]) textNodes.push({ node, english: node.nodeValue, key });
}

let currentLanguage = 'ar';
const menuLabel = (open) => currentLanguage === 'ar' ? (open ? 'إغلاق القائمة' : 'فتح القائمة') : (open ? 'Close navigation' : 'Open navigation');

function applyLanguage(language) {
  currentLanguage = language === 'en' ? 'en' : 'ar';
  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';

  textNodes.forEach(({ node, english, key }) => {
    node.nodeValue = currentLanguage === 'ar' ? english.replace(key, translations[key]) : english;
  });

  document.title = pageMeta[currentLanguage].title;
  const description = document.querySelector('meta[name="description"]');
  if (description) description.content = pageMeta[currentLanguage].description;

  document.querySelectorAll('[data-language]').forEach(button => {
    button.setAttribute('aria-pressed', String(button.dataset.language === currentLanguage));
  });

  const whatsappUrl = `https://wa.me/96894178871?text=${encodeURIComponent(pageMeta[currentLanguage].orderMessage)}`;
  document.querySelectorAll('[data-whatsapp-order]').forEach(link => link.href = whatsappUrl);

  const toggle = document.querySelector('[data-menu-toggle]');
  if (toggle) toggle.setAttribute('aria-label', menuLabel(toggle.getAttribute('aria-expanded') === 'true'));
  localStorage.setItem('sultan-language', currentLanguage);
}

document.querySelectorAll('[data-language]').forEach(button => {
  button.addEventListener('click', () => applyLanguage(button.dataset.language));
});

const header = document.querySelector('[data-header]');
const toggle = document.querySelector('[data-menu-toggle]');
const nav = document.querySelector('[data-nav]');

const updateHeader = () => header.classList.toggle('scrolled', window.scrollY > 24);
updateHeader();
window.addEventListener('scroll', updateHeader, { passive: true });

toggle.addEventListener('click', () => {
  const open = nav.classList.toggle('open');
  toggle.setAttribute('aria-expanded', String(open));
  toggle.setAttribute('aria-label', menuLabel(open));
  document.body.classList.toggle('menu-open', open);
});

nav.querySelectorAll('a').forEach(link => link.addEventListener('click', () => {
  nav.classList.remove('open');
  toggle.setAttribute('aria-expanded', 'false');
  toggle.setAttribute('aria-label', menuLabel(false));
  document.body.classList.remove('menu-open');
}));

document.querySelector('[data-year]').textContent = new Date().getFullYear();
applyLanguage(localStorage.getItem('sultan-language') || 'ar');

if ('IntersectionObserver' in window && !window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });
  document.querySelectorAll('.reveal').forEach(item => observer.observe(item));
} else {
  document.querySelectorAll('.reveal').forEach(item => item.classList.add('visible'));
}
